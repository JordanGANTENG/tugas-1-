# Aritmatika

x = 24
y = 67


# operasi pengurangan 
hasil = x - y 
print ("nilai dari x - y = ", hasil)

# operasi penjumlahan
hasil = x + y
print ("nilai dari x + y = ", hasil)

# operasi pembagian
hasil = x / y
print ("nilai dari x / y = ", hasil)

# operasi perkalian
hasil = x * y
print ("nilai dari x * y = ", hasil)

# eksponen (perpangkatan)
hasil = x ** y
print ("nilai dari x ** y = ", hasil)

# modulus (sisa bagi)
hasil = x % y
print ("nilai dari x % y = ", hasil)

# floor division
hasil = x // y
print ("nilai dari x // y = ", hasil)
